				<li><span class="quote-ilek">Ασφαλείας ή απλές:</span> Αυτές οι πρίζες έχουν πάντα καπάκι για λόγους ασφάλειας ώστε να προστατεύονται από τα νερά κτλ </li>
			</ul>


			<h4>Οι πρίζες επίσης διαφοροποιούνται ανάλογα με το τύπο ρεύματος που έχετε:</h4>

			<ul>
				<li>Αν είστε κατοικία ή απλό μαγαζί τότε έχετε μονοφασικό ρεύμα με κοινό ρεύμα στα 220V.</li>
				<li>2.	Aν είστε επιχείρηση ή βιοτεχνία που τότε έχετε τριφασικό ρεύμα στα 380V και χρειάζεστε ειδικές πρίζες.</li>
			</ul>

		    <h3>Πως επιλέγουμε πρίζες</h3>
		    <p>Όταν θα αγοράσουμε πρίζες καλό θα ήταν να αγοράζετε μόνο επώνυμες πρίζες και όχι φτηνές απομιμήσεις γιατί οι φτηνές χαλάνε γρήγορα. Επίσης, μια επώνυμη και καλή πρίζα μπορεί να μας εγγυηθεί ασφάλεια και εγγύηση στο χρόνο. Όσο φτηνές και αν σας φαίνονται τώρα οι φτηνές απομιμήσεις μακροπρόθεσμα θα σας κοστίσουν περισσότερο.</p>

		    <h3>Μερικά πράγματα που πρέπει να γνωρίζουμε για την αλλαγή της πρίζας</h3>
		    <ul>
		    	<li>Πριν προχωρήσουμε σε οποιαδήποτε εργασία στα ηλεκτρολογικά μας θα πρέπει να σιγουρευτούμε ότι ο ηλεκτρολόγος έχει τσεκάρει ότι τα ηλεκτρολογικά μας δεν αντιμετωπίζουν κάποιο πρόβλημα. Αν αντιμετωπίζετε κάποιο πρόβλημα ο ηλεκτρολόγος σας θα πρέπει να σας συμβουλεύσει τι να κάνετε. </li>
		    	<li>Τσεκάρετε πάντα τις διαστάσεις. Μπορεί να μοιάζουν όλες οι πρίζες ίδιες αλλά δεν είναι. Από μάρκα σε μάρκα έχουμε διαφορές σε πλάτος και μήκος. Έτσι πρέπει πριν την αλλαγή πρίζας να έχουμε πάρει τις ακριβείς διαστάσεις ώστε να μην έχουμε προβλήματα μετά και μας μείνουν άχρηστες πρίζες και έχουμε ξοδέψει λεφτά χωρίς λόγο.</li>
		    </ul>

		    <p>Ο ηλεκτρολόγος που θα επιλέξουμε θα πρέπει να έχει την εξειδίκευση να μας ενημερώσει για την αλλαγή πρίζας και να προβεί στην εργασία της αλλαγής πρίζας. Στο MyConstructor θα βρούμε αξιολογημένους ηλεκτρολόγους που θα κάνουν την αλλαγή πρίζας και όποια άλλη ηλεκτρολογική εργασία χρειαστούμε εύκολα, άμεσα και οικονομικά.</p>


