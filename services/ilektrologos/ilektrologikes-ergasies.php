			<p>Για την αλλαγή/αντικατάσταση διακόπτη ακολουθούμε σχεδόν την ίδια διαδικασία με την αλλαγή πρίζας λόγω του ότι και τα δύο αποτελούνται από μηχανισμό και καπάκι. Στον εσωτερικό τους μηχανισμό υπάρχουν 2 ή 3 αγωγοί: ο αγωγός της φάσης και η συνέχεια της φάσης, η λεγόμενη επιστροφή. </p>
			

