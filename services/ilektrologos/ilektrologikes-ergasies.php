			<h2 id="priza">Αλλαγή πρίζας</h2>
			<p>Οποιαδήποτε ηλεκτρολογική εργασία που κάνουμε έχει ένα μεγάλο βαθμό επικινδυνότητας. Πριν αποφασίσουμε να προβούμε σε οποιαδήποτε εργασία που έχει σχέση με τα ηλεκτρολογικά θα πρέπει να συμβουλευτούμε έναν ηλεκτρολόγο. Τα λάθη σε οτιδήποτε αφορούν το ρεύμα έχουν μπορεί να μας κοστίσουν όχι μόνο σε κόστος αλλά και να πάθουμε κάτι πολύ σοβαρό, πυρκαγιά ή βραχυκύκλωμα είναι κάτι που μπορεί να συμβεί πολύ εύκολα αν κάνουμε κάτι λάθος.</p>

			<p>Από τις φαινομενικά απλές ηλεκτρολογικά εργασίες είναι η αλλαγή πρίζας. Παρόλα αυτά δεν είναι. Πρέπει οπωσδήποτε να αναζητήσουμε την βοήθεια ενός ειδικού ηλεκτρολόγου και να μην επιχειρήσουμε να προβούμε στην αλλαγή πρίζας μόνοι μας. </p>
			

			<h4>Αρχικά πρέπει να γνωρίζουμε ότι υπάρχουν δύο είδη πρίζας:</h4>

